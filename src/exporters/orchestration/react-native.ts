  DESIGN_REFERENCE_LAYOUT,
  FORBIDDEN_GENERIC_RENDERER,
  FORBIDDEN_TEMPLATE_BATCHING,
  FORBIDDEN_VISUAL_SHORTCUTS,
  MANDATORY_SCREEN_WORKFLOW,
